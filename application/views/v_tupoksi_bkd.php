			<p>&nbsp;</p>

			<p><em>Sesuai dengan (PP No. 159/2000) dan&nbsp;<a href="http://simpeg.bkd.jatengprov.go.id/ppid/assets/upload/files/PERGUB%2083%20TH%202016.pdf"> Peraturan Gubernur Jawa Tengah No 83 Tahun 2016 tentang Organisasi dan Tata Kerja Badan Kepegawaian Daerah Provinsi Jawa Tengah</a></em></p>

			<p>BKD mempunyai tugas pokok untuk membantu Gubernur melaksanakan fungsi penunjang urusan pemerintahan dibidang kepegawaian yang menjadi kewenangan daerah.</p>

			<p>Untuk menyelenggarakan tugas sebagaiamana tersebut diatas, Badan Kepegawaisn Daerah Provinsi Jawa Tengah mempunyai fungsi sebagai berikut :</p>

			<ol>
				<li>penyusunan kebijakan teknis di bidang perencanaan dan pengembangan pegawai, mutasi, pembinaan dan kesejahteraan pegawai, dan informasi kepegawaian;</li>
				<li>pelaksanaan tugas dukungan teknis di bidangperencanaan dan pengembangan pegawai, mutasi, pembinaan dan kesejahteraan pegawai dan informasi kepegawaian;</li>
				<li>pemantauan, evaluasi, dan pelaporan pelaksanaan tugas dukungan teknis di bidang perencanaan dan pengembangan pegawai, mutasi, pembinaan dan kesejahteran pegawai, dan informasi kepegawaian;</li>
				<li>pembinaan teknis penyelenggaraan fungsi penunjang urusan pemerintahan Daerah di bidang perencanaan dan pengembangan pegawai, mutasi, pembinaan dan kesejahteraan pegawai dan informasi kepegawaian;</li>
				<li>pelaksanaan dan pembinaan administrasi kepada seluruh unit kerja dilingkungan Badan; dan</li>
				<li>pelaksanaan fungsi lain yang diberikan oleh Gubernur sesuai dengan tugas dan fungsinya.</li>
			</ol>

			<p>BADAN KEPEGAWAIAN DAERAH membawahkan :</p>

